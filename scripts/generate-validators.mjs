import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import Ajv from 'ajv'
import standaloneCode from 'ajv/dist/standalone/index.js'

const schemaUrl = new URL('../resources/schemas/prompt-concept.schema.json', import.meta.url)
const outputUrl = new URL(
  '../src/features/prompt-library/validatePromptConcept.generated.ts',
  import.meta.url,
)
const schema = JSON.parse(await readFile(schemaUrl, 'utf8'))
const ajv = new Ajv({ allErrors: true, code: { esm: true, source: true } })
const validate = ajv.compile(schema)
const generated = standaloneCode(ajv, validate).replace(
  /const (func\d+) = require\("([^"]+)"\)\.default;/g,
  (_match, name, modulePath) => {
    if (modulePath === 'ajv/dist/runtime/ucs2length') {
      return `const ${name} = (value) => Array.from(value).length;`
    }
    if (modulePath === 'ajv/dist/runtime/equal') {
      return `const ${name} = (left, right) => {
  if (left === right) return true;
  if (!left || !right || typeof left !== "object" || typeof right !== "object") return false;
  if (Array.isArray(left)) {
    return Array.isArray(right) && left.length === right.length && left.every((value, index) => ${name}(value, right[index]));
  }
  if (Array.isArray(right)) return false;
  const keys = Object.keys(left);
  return keys.length === Object.keys(right).length && keys.every((key) => Object.prototype.hasOwnProperty.call(right, key) && ${name}(left[key], right[key]));
};`
    }
    throw new Error(`Unsupported Ajv runtime helper: ${modulePath}`)
  },
)
if (generated.includes('require(') || generated.includes('new Function')) {
  throw new Error('Generated validator contains CSP-unsafe runtime code')
}
const banner = [
  '// Generated by scripts/generate-validators.mjs. Do not edit.',
  '// @ts-nocheck',
  '/* eslint-disable */',
  '',
].join('\n')

await writeFile(fileURLToPath(outputUrl), `${banner}${generated}\n`)
